import Foundation

public final class SwiftLib{
    
    func sumTwoNumber(firstNumber : Int , secoundNumber : Int)-> Int{
        return firstNumber + secoundNumber
    }
    
    func subTwoNumber(firstNumber : Int , secoundNumber : Int)-> Int{
        return firstNumber - secoundNumber
    }
}
